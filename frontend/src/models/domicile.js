function Domicile (domicileData) {
    this.streetAddress = domicileData.streetAddress
    this.streetNumber = domicileData.streetNumber
    this.city = domicileData.city
    this.floor = domicileData.floor
    this.postalCode = domicileData.postalCode
    this.country = domicileData.country
}

export { Domicile }